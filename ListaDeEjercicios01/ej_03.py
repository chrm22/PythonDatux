num1=int(input("Ingrese el primer número: "))
num2=int(input("Ingrese el segundo número: "))
num3=int(input("Ingrese el tercer número: "))

print("Suma -------------> " + str(num1) + " + " + str(num2) + " + " + str(num3) + " = " + str(num1+num2+num3))
print("Resta ------------> " + str(num1) + " - " + str(num2) + " - " + str(num3) + " = " + str(num1-num2-num3))
print("Multiplicación ---> " + str(num1) + " * " + str(num2) + " * " + str(num3) + " = " + str(num1*num2*num3))
print("División ---------> " + str(num1) + " ÷ " + str(num2) + " ÷ " + str(num3) + " = " + str(num1/num2/num3))
print("División entera --> " + str(num1) + " // " + str(num2) + " // " + str(num3) + " = " + str(num1//num2//num3))